public class Strings {
    public static void main(String[] args) {

        int x = 5; // примитивные типы данных с маленькой буквы

        String s = "Hello"; // строка это класс ... переменная s сылается на объект "Hello"
        String space = " ";
        String name = "Bob";

        System.out.println(s + space + name);
        System.out.println("Hello" + " " + "John");  // sout + tab
        System.out.println("My numbers is " + x);     
    }
}
